#include <stdio.h>

int main() {
    int x;
    double y;
    scanf("%d",&x);
    if (x <= 150) y = x * 0.4463;
    else if (x <= 400) y = (x - 150) * 0.4663 + 150 * 0.4463;
    else y = (x - 400) * 0.5663 + (400 - 150) * 0.4663 + 150 * 0.4463;
    printf("%.1f\n",y);
    return 0;
}
